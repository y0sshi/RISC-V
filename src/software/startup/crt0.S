// =============================================================================
// crt0.S - Bare-metal startup for rv_soc (RV32IM / RV64IM)
// =============================================================================
//
// Hardware memory layout (rv_soc.ld):
//   IMEM 0x0000_0000 (16 KB) : .text のみ  → IMEM_FILE
//   DMEM 0x8000_0000 (16 KB) : .rodata / .data / .bss / stack → DMEM_FILE
//
// NOTE: IMEM は命令フェッチ専用。データポート経由では読めない。
//       そのため .rodata (文字列リテラル等) は必ず DMEM に配置する。
//       .data は DMEM_FILE でプリロード済みのためコピー不要。
//       起動時に必要なのは .bss のゼロクリアのみ。
//
// Boot sequence:
//   1. mtvec をトラップハンドラに設定 (例外を捕捉してデバッグ容易化)
//   2. 割り込み無効化 (MIE = 0)
//   3. global pointer 設定 (GP-relative リラクゼーション用)
//   4. stack pointer 設定 (DMEM 末尾)
//   5. .bss ゼロクリア (DMEM 内)
//   6. main() 呼び出し
//   7. 戻り値は無視して無限ループ
//
// Author: Naofumi Yoshinaga
// =============================================================================

    .section .text.start, "ax", @progbits
    .global  _start
    .type    _start, @function

_start:
    // ---------------------------------------------------------------
    // 1. mtvec: トラップベクタを設定 (direct mode, bit[1:0]=00)
    // ---------------------------------------------------------------
    la      t0, _trap_handler
    csrw    mtvec, t0

    // ---------------------------------------------------------------
    // 2. 全割り込み無効化
    // ---------------------------------------------------------------
    csrw    mie, zero

    // ---------------------------------------------------------------
    // 3. global pointer 設定
    //    .option norelax でチキンエッグ問題を回避
    // ---------------------------------------------------------------
    .option push
    .option norelax
    la      gp, __global_pointer$
    .option pop

    // ---------------------------------------------------------------
    // 4. stack pointer = DMEM 末尾 (_stack_top)
    // ---------------------------------------------------------------
    la      sp, _stack_top

    // ---------------------------------------------------------------
    // 5. .bss ゼロクリア (DMEM 内)
    //    .data は DMEM_FILE でプリロード済みなのでコピー不要
    // ---------------------------------------------------------------
    la      a0, _bss_start
    la      a1, _bss_end
    bgeu    a0, a1, .Lcall_main     // .bss が空なら skip

.Lzero_bss:
    sw      zero, 0(a0)
    addi    a0, a0, 4
    bltu    a0, a1, .Lzero_bss

    // ---------------------------------------------------------------
    // 6. main(0, NULL) 呼び出し
    // ---------------------------------------------------------------
.Lcall_main:
    li      a0, 0                   // argc = 0
    li      a1, 0                   // argv = NULL
    call    main

    // ---------------------------------------------------------------
    // 7. main() から戻ったら halt
    //    _exit() もここにジャンプする
    // ---------------------------------------------------------------
    .global _halt
_halt:
    j       _halt


// =============================================================================
// _trap_handler: 最小トラップハンドラ
//   mcause を mscratch に退避して無限ループ
//   (GTKWave で mscratch を確認することでトラップ原因が分かる)
// =============================================================================
    .global  _trap_handler
    .type    _trap_handler, @function
    .align   2

_trap_handler:
    csrw    mscratch, a0            // a0 を一時保存
    csrr    a0, mcause              // トラップ原因を読み出し
    csrw    mscratch, a0            // mscratch に保存 (波形で確認可能)

.Ltrap_loop:
    j       .Ltrap_loop
